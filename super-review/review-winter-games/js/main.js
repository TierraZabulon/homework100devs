//Create a function that takes in an array of numbers. Return a new array containing every even number from the original array (do not use map or filter)
function takeIn(arr) {
  let onlyEven = [];
  let arrOfNums = arr.forEach((x) => {
    return x;
  });
  if (arrOfNums % 2 === 0) {
    arr.push(onlyEven);
  }
}

